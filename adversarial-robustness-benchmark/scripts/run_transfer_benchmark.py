"""CLI: evaluate all 7 models on the transfer-poisoned set.

Phase B runner — black-box transferability. The adversarial images are
pre-generated by ``scripts/generate_datasets.py --transfer`` using PGD on
the ResNet-50 surrogate and shared across all target models. This script
does inference only: no adversarial generation, no gradients required.

For each model it computes:
  * clean accuracy     — on the surrogate-preprocessed clean images
  * transfer robust accuracy — on the surrogate-crafted adversarial images
  * accuracy drop
  * fooling rate (originally-correct images that flipped)

The ResNet-50 row is the white-box reference: its result should match the
gradient phase's PGD number within ±0.005 (Gate B sanity check).

Per-model JSONs are the resumption unit — re-running skips finished models
unless --force is passed.

Usage:
    python scripts/generate_datasets.py --transfer   # craft adversarial set first
    python scripts/run_transfer_benchmark.py
    python scripts/run_transfer_benchmark.py --models resnet50,vit_b_16
    python scripts/run_transfer_benchmark.py --force
"""

from __future__ import annotations

import argparse
import json
import os
import random
import sys
import time

import numpy as np
import torch
import yaml

_REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if _REPO_ROOT not in sys.path:
    sys.path.insert(0, _REPO_ROOT)

from datasets.loader import load_transfer_dataset  # noqa: E402
from models.classifiers import build_classifier    # noqa: E402

_DEFAULT_SURROGATE = "resnet50"

# Forward-pass-only batch sizes — no backprop, so wider than gradient runner.
_INFER_BATCH = {
    "resnet50": 64,
    "vgg16": 32,
    "convnext_tiny": 32,
    "vit_b_16": 32,
    "swin_t": 32,
    "efficientnet_b0": 64,
    "clip_vit_b16": 32,
}


def load_config() -> dict:
    with open(os.path.join(_REPO_ROOT, "config.yaml"), "r", encoding="utf-8") as f:
        return yaml.safe_load(f)


def set_seed(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)


@torch.no_grad()
def predict_all(clf, batch_0_1: torch.Tensor, batch_size: int = 64) -> torch.Tensor:
    """Return argmax predictions (CPU long tensor) for the full batch."""
    preds = []
    for start in range(0, batch_0_1.shape[0], batch_size):
        x = batch_0_1[start:start + batch_size].to(clf.device)
        preds.append(clf.logits(x).argmax(dim=1).cpu())
    return torch.cat(preds).long()


def run_one(
    model_key: str,
    transfer_ds,
    cfg: dict,
    out_dir: str,
    force: bool,
) -> str:
    out_path = os.path.join(out_dir, f"{model_key}.json")
    if not force and os.path.exists(out_path):
        print(f"  skip  {model_key}  (cached at {out_path})")
        return out_path

    set_seed(int(cfg["seed"]))
    device = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"[{model_key}] loading model on {device} ...")
    clf = build_classifier(model_key, device)

    clean_batch = transfer_ds.clean_batch   # (N, 3, H, W) in [0,1], CPU
    adv_batch = transfer_ds.adv_batch       # same shape
    labels = transfer_ds.labels_tensor      # (N,) long, CPU
    bs = _INFER_BATCH.get(model_key, 32)

    t0 = time.perf_counter()
    clean_preds = predict_all(clf, clean_batch, batch_size=bs)
    t_clean = time.perf_counter() - t0

    t0 = time.perf_counter()
    adv_preds = predict_all(clf, adv_batch, batch_size=bs)
    t_adv = time.perf_counter() - t0

    clean_correct = int((clean_preds == labels).sum().item())
    clean_acc = clean_correct / len(labels)
    print(f"  clean:           {clean_correct}/{len(labels)} = {clean_acc:.3f}  ({t_clean:.1f}s)")

    robust_correct = int((adv_preds == labels).sum().item())
    robust_acc = robust_correct / len(labels)
    print(f"  transfer robust: {robust_correct}/{len(labels)} = {robust_acc:.3f}  ({t_adv:.1f}s)")

    orig_correct_mask = clean_preds == labels
    n_orig_correct = int(orig_correct_mask.sum().item())
    flipped = int(((clean_preds != adv_preds) & orig_correct_mask).sum().item())
    fooling_rate = flipped / n_orig_correct if n_orig_correct else 0.0
    print(f"  fooling rate:    {flipped}/{n_orig_correct} = {fooling_rate:.3f}")

    host_env = {
        "gpu": torch.cuda.get_device_name(0) if torch.cuda.is_available() else "CPU",
        "cuda_available": bool(torch.cuda.is_available()),
        "torch": torch.__version__,
    }
    try:
        import torchvision
        host_env["torchvision"] = torchvision.__version__
    except ImportError:
        pass

    record = {
        "model": model_key,
        "surrogate": transfer_ds.surrogate,
        "attack": "transfer_pgd",
        "is_white_box": model_key == transfer_ds.surrogate,
        "epsilon": transfer_ds.epsilon,
        "num_steps": transfer_ds.num_steps,
        "seed": int(cfg["seed"]),
        "dataset_size": len(labels),
        "clean_correct": clean_correct,
        "robust_correct": robust_correct,
        "clean_accuracy": clean_acc,
        "transfer_robust_accuracy": robust_acc,
        "accuracy_drop": clean_acc - robust_acc,
        "n_originally_correct": n_orig_correct,
        "n_flipped_from_correct": flipped,
        "fooling_rate": fooling_rate,
        "host_env": host_env,
        "wall_clock_s": {
            "clean_eval": t_clean,
            "adv_eval": t_adv,
        },
    }

    os.makedirs(out_dir, exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(record, f, indent=2)
    print(f"  wrote {out_path}")

    del clf
    if torch.cuda.is_available():
        torch.cuda.empty_cache()
    return out_path


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--models", type=str, default=None)
    parser.add_argument("--surrogate", type=str, default=_DEFAULT_SURROGATE,
                        help="Surrogate model key to load adversarial set from.")
    parser.add_argument("--force", action="store_true",
                        help="Re-run models whose JSON already exists.")
    parser.add_argument("--output-dir", type=str, default=None)
    args = parser.parse_args()

    cfg = load_config()
    models = [m.strip() for m in args.models.split(",")] if args.models else list(cfg["models"])
    out_dir = args.output_dir or os.path.join(_REPO_ROOT, "results", "transfer")
    os.makedirs(out_dir, exist_ok=True)

    print("=== Loading transfer adversarial set ===")
    transfer_ds = load_transfer_dataset(surrogate=args.surrogate)
    print(f"  images   : {len(transfer_ds)}")
    print(f"  epsilon  : {transfer_ds.epsilon:.6f}  ({round(transfer_ds.epsilon * 255)}/255)")
    print(f"  steps    : {transfer_ds.num_steps}")
    print(f"  surrogate: {transfer_ds.surrogate}")

    print(f"\nmodels : {models}")
    print(f"output : {out_dir}\n")

    failures = []
    t0 = time.perf_counter()
    for model_key in models:
        try:
            run_one(model_key, transfer_ds, cfg, out_dir, args.force)
        except Exception as exc:
            print(f"  FAIL  {model_key}: {exc}")
            failures.append((model_key, repr(exc)))

    elapsed = time.perf_counter() - t0
    print(f"\nDone in {elapsed/60:.1f} min  failures: {len(failures)}")
    for m, e in failures:
        print(f"  - {m}: {e}")


if __name__ == "__main__":
    main()
